<template>
  <v-dialog
    :value="value"
    @input="$emit('input', $event)"
    max-width="750"
    overlay-color="var(--v-overlay-base)"
    overlay-opacity="0.3"
    persistent
  >
    <v-form ref="form">
      <v-card class="pb-3">
        <v-card-title>
          {{ id ? lang.TITLE_EDIT_CUSTOMER : lang.TITLE_ADD_CUSTOMER }}
        </v-card-title>
        <v-card-text class="pt-2">
          <v-form style="max-width: 400px">
            <v-text-field
              v-model="customerName"
              label="Имя клиента"
              outlined
              dense
            />
            <div class="d-flex justify-space-between">
              <v-text-field
                v-model="customerCost"
                label="Стоимость встречи"
                outlined
                dense
                style="max-width: 250px; margin-right: 16px"
              />
              <v-select
                v-model="customerCurrency"
                :items="currencies"
                label="Валюта"
                item-text="name"
                item-value="id"
                style="max-width: 100px"
                outlined
                dense
              />
            </div>
            <v-btn @click="addTime()" class="mb-1" small depressed
              >Добавить время</v-btn
            >
            <div class="mt-4" v-if="customerMeetingTimes.length">
              <div
                v-for="item in customerMeetingTimes"
                :key="item.timestamp"
                class="d-flex"
              >
                <v-select
                  v-model="item.dayOfWeek"
                  :items="daysOfWeek"
                  item-text="rusName"
                  item-value="name"
                  label="День недели"
                  class="mr-2"
                  style="max-width: 170px"
                  outlined
                  dense
                />
                <v-select
                  v-model="item.hour"
                  :items="meetingHours"
                  item-text="rusName"
                  item-value="name"
                  label="Часы"
                  class="mr-2"
                  style="max-width: 100px"
                  outlined
                  dense
                />
                <v-select
                  v-model="item.minute"
                  :items="meetingMinutes"
                  item-text="rusName"
                  item-value="name"
                  label="Минуты"
                  class="mr-2"
                  style="max-width: 120px"
                  outlined
                  dense
                />
                <v-btn @click="deleteTime(item.timestamp)" icon
                  ><v-icon>mdi-delete</v-icon></v-btn
                >
              </div>
            </div>
          </v-form>
        </v-card-text>
        <v-card-actions class="d-flex justify-end pr-6">
          <v-btn depressed class="mr-5" @click="close">
            {{ lang.BUTTON_CLOSE }}
          </v-btn>
          <v-btn @click="save" color="primary" depressed>{{
            lang.BUTTON_SAVE
          }}</v-btn>
        </v-card-actions>
      </v-card>
    </v-form>
  </v-dialog>
</template>

<script>
import { lang } from "@/settings/lang";
import { apiRequest } from "@/api/api";
import { API_ROUTES } from "@/settings/api";
import { toastError, toastSuccess } from "@/helpers/toasts";
import { daysOfWeek, meetingHours, meetingMinutes } from "@/settings/dates";
export default {
  name: "CustomerCard",
  components: {},
  props: {
    value: Boolean,
    id: Number,
  },
  data() {
    return {
      text: "",
      lang,
      customer: "",
      daysOfWeek,
      meetingHours,
      meetingMinutes,
      newDayOfWeek: "",
      newHour: "",
      newMinute: "",
      customerMeetingTimes: [],
      emptyItem: {
        dayOfWeek: "",
        hour: "",
        minute: "",
      },
      customerName: "",
      customerCost: "",
      customerCurrency: "",
      currencies: ["GEL", "RUB", "USD", "EUR"],
    };
  },
  computed: {
    customers() {
      return [
        {
          id: 1,
          name: "Ольга Ивановна",
          price: {
            amount: 4000,
            currency: "rub",
          },
        },
        {
          id: 2,
          name: "Андрей Петрович",
        },
      ];
    },
    selectedCustomer() {
      return this.customers.find((el) => el.id === this.customer);
    },
  },
  watch: {
    id(newVal) {
      if (newVal) {
        this.getCustomer(newVal);
        this.$emit("input", true);
      }
    },
  },
  methods: {
    close() {
      this.$emit("input", false);
      this.clean();
      this.$emit("update:id", null);
    },
    async save() {
      const res = await apiRequest({
        path: API_ROUTES.CUSTOMER,
        method: "post",
        data: {
          id: this.id,
          name: this.customerName,
          cost: this.customerCost,
          currency: this.customerCurrency,
          times: this.customerMeetingTimes,
        },
      });
      if (res.success) {
        this.$emit("updateList");
        toastSuccess(
          this.id ? lang.CUSTOMER_UPDATE_SUCCESS : lang.CUSTOMER_CREATE_SUCCESS
        );
        this.close();
      }
    },
    clean() {
      this.text = "";
    },
    async getCustomer() {
      const res = await apiRequest({
        path: `${API_ROUTES.CUSTOMER}/${this.id}`,
      });
      if (res.success) {
        this.text = res.data.text;
      } else {
        toastError(lang.UNKNOWN_ERROR);
      }
    },
    async addTime() {
      this.customerMeetingTimes.push({
        ...this.emptyItem,
        timestamp: Date.now(),
      });
      console.log(this.customerMeetingTimes);
    },
    async deleteTime(timestamp) {
      this.customerMeetingTimes = this.customerMeetingTimes.filter(
        (el) => el.timestamp !== timestamp
      );
    },
  },
};
</script>

